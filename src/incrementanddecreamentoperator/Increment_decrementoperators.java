package incrementanddecreamentoperator;

public class Increment_decrementoperators {

	public static void main(String[] args) {
		
		int a=10; int b=20;
		a++;
		b++;
		System.out.println(a);
		System.out.println(b);
		
		a=10;
		//a++;
		a--;
		System.out.println(a);
		
		b=20;
		//b++;
		b--;
		System.out.println(b);
		
		
		
		
		
		
	}

}
