package mypack;

public class Relationaloperator {

	public static void main(String[] args) {
		
		int a = 30;
		int b = 20;
		
		// Relational(Comparison operators  --> ==, <,>, <=, >=, != //
		// Always return boolean value//
		
		System.out.println(a==b);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a!=b);
	}

}
